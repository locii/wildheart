# Can AI do therapy and is it an existential risk?

The author explores whether artificial intelligence can perform psychotherapy, noting that while LLMs handle approximately 80% of cognitive work, they lack true co-regulation capabilities essential to therapeutic healing.

## AI's Current Strengths in Therapy

AI systems excel at cognitive functions including pattern recognition, reflective responses, and conceptual reframing. Applications like Wysa and Woebot already provide meaningful support for mild anxiety and loneliness by handling "summarising narratives" and offering "empathic language."

## The Co-Regulation Gap

The critical distinction lies in nervous system interaction. Modern neuroscience suggests healing emerges through "nervous system attunement" and "regulated relational presence" — aspects requiring human embodied interaction. Text-based AI cannot replicate the physiological components of therapeutic connection, such as facial expressions, vocal tone, and breathing synchrony.

## Future Therapeutic Structure

The article proposes three emerging layers:

1. **Mass-scale AI support** for routine emotional challenges
2. **AI-augmented therapy** assisting human practitioners
3. **Essential human therapy** for trauma, attachment work, and embodied practices

## The Real Risk

Rather than replacement, the primary concern is commodification — cheap AI becoming the default mental health interface globally, potentially relegating human therapy to expensive specialization while paradoxically increasing its perceived value.

Human therapeutic presence may remain irreplaceable, particularly for practices emphasizing embodied work like breathwork and somatic therapy.
